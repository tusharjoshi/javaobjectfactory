package com.tusharjoshi.javatools.objectfactory.example;

public class Animal {

  private String species;

  private int legs;

  public String getSpecies() {
    return species;
  }

  public void setSpecies(String species) {
    this.species = species;
  }

  public int getLegs() {
    return legs;
  }

  public void setLegs(int legs) {
    this.legs = legs;
  }

}
